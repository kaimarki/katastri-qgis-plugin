class CadastreVersionViewer:
    """QGIS Plugin Implementation."""

    # A cadastre identifier looks like 78401:101:4011 (digits:digits:digits).
    TUNNUS_RE = re.compile(r"^\d+:\d+:\d+$")
    # Maa- ja Ruumiamet address search (gazetteer) service.
    GAZETTEER_URL = "https://aks.geoportaal.ee/inaks/inaadress/gazetteer"
    # The gazetteer rejects the default urllib user agent with HTTP 403.
    HTTP_HEADERS = {
        "User-Agent": "QGIS CadastreVersionViewer",
        "Referer": "https://aks.geoportaal.ee/inaks/inaadress",
    }
    # Outline colors cycled over the version layers loaded into one group.
    VERSION_COLORS = ["#e41a1c", "#377eb8", "#4daf4a", "#984ea3",
                      "#ff7f00", "#a65628"]

