    def set_results_mode(self, mode):
        """Switch the panel between version results and border-point results."""
        is_point = (mode == "point")
        self.panel.versionTable.setVisible(not is_point)
        self.panel.selectAllButton.setVisible(not is_point)
        self.panel.loadButton.setVisible(not is_point)
        self.panel.pointTable.setVisible(is_point)

    def search_border_point(self, nr):
        """Look up a border point by piiripunkti_nr, show it on the map and list
        its attributes."""
        base_url = (
            "https://gsavalik.envir.ee/geoserver/kataster/wfs"
            "?service=WFS"
            "&version=1.0.0"
            "&request=GetFeature"
            "&typeName=kataster:ky_punktid_kehtiv"
            "&outputFormat=application/json"
            "&CQL_FILTER=piiripunkti_nr=" + urllib.parse.quote(str(nr))
        )

        self.panel.setCursor(Qt.WaitCursor)
        try:
            req = urllib.request.Request(base_url, headers=self.HTTP_HEADERS)
            with urllib.request.urlopen(req) as response:
                geojson = response.read().decode("utf-8")
        except Exception as e:
            self.iface.messageBar().pushMessage(
                self.tr(u'Kataster'),
                self.tr(u'Piiripunkti otsing ebaõnnestus: {}').format(e),
                level=Qgis.Critical, duration=6)
            return
        finally:
            self.panel.unsetCursor()

        try:
            data = json.loads(geojson)
        except Exception:
            self.iface.messageBar().pushMessage(
                self.tr(u'Kataster'),
                self.tr(u'Vastust ei õnnestunud lugeda.'),
                level=Qgis.Critical, duration=6)
            return

        features = data.get("features", [])
        if not features:
            self.iface.messageBar().pushMessage(
                self.tr(u'Kataster'),
                self.tr(u'Piiripunkti nr "{}" ei leitud.').format(nr),
                level=Qgis.Warning, duration=5)
            return

        # A border point is shared between neighbouring units, so several
        # features can come back for the same physical point.
        self.set_results_mode("point")
        self.display_point_attributes([f.get("properties", {}) for f in features])
        self.show_point_on_map(geojson, nr)
        self.iface.messageBar().pushMessage(
            self.tr(u'Kataster'),
            self.tr(u'Piiripunkt {} leitud.').format(nr),
            level=Qgis.Success, duration=4)

    def display_point_attributes(self, props_list):
        """Fill the key/value table with the border point's attributes."""
        first = props_list[0]
        tunnused = u", ".join(sorted({
            str(p.get("tunnus")) for p in props_list if p.get("tunnus")}))

        rows = [
            (self.tr(u'Piiripunkti nr'), first.get("piiripunkti_nr")),
            (self.tr(u'Katastritunnus(ed)'), tunnused),
            (self.tr(u'X'), first.get("x")),
            (self.tr(u'Y'), first.get("y")),
            (self.tr(u'Piiripunkt'), first.get("piiripunkt")),
            (self.tr(u'Mõõtmise aeg'), self.format_date(first.get("punkt_moot_aeg"))),
            (self.tr(u'Mõõtmisviis'), first.get("mootviis")),
            (self.tr(u'Mõõtja'), first.get("mootja")),
            (self.tr(u'Vajadus asukoht selgitada'),
             self.format_bool(first.get("vajadus_asukoht_selgitada"))),
        ]

        table = self.panel.pointTable
        table.clearContents()
        table.setRowCount(len(rows))
        for row_idx, (key, value) in enumerate(rows):
            table.setItem(row_idx, 0, QTableWidgetItem(str(key)))
            text = "" if value is None else str(value)
            table.setItem(row_idx, 1, QTableWidgetItem(text))
        table.resizeColumnToContents(0)

    def show_point_on_map(self, geojson, nr):
        """Add the border point as a layer and zoom the map canvas to it."""
        layer = QgsVectorLayer(geojson, u"Piiripunkt {}".format(nr), "ogr")
        if not layer.isValid():
            return  # attributes are already shown; the map is best-effort
        QgsProject.instance().addMapLayer(layer, False)
        self.get_layer_group(self.tr(u'Piiripunktid')).insertLayer(0, layer)
        try:
            canvas = self.iface.mapCanvas()
            layer.selectAll()
            canvas.zoomToSelected(layer)
            layer.removeSelection()
            canvas.zoomScale(1000)  # a single point needs a sensible scale
            canvas.refresh()
        except Exception:
            pass

    @staticmethod
    def format_bool(value):
        if value is True:
            return "Jah"
        if value is False:
            return "Ei"
        return ""

    @staticmethod
    def format_date(value):
        # WFS dates arrive like "2003-06-11Z"; drop the trailing marker.
        if isinstance(value, str) and value.endswith("Z"):
            return value[:-1]
        return "" if value is None else str(value)

    def toggle_select_all(self):
        """Check all rows, or uncheck them if they are already all checked."""
        table = self.panel.versionTable
        rows = range(table.rowCount())
        all_checked = rows and all(
            table.item(r, 0).checkState() == Qt.Checked for r in rows)
        new_state = Qt.Unchecked if all_checked else Qt.Checked
        for r in rows:
            table.item(r, 0).setCheckState(new_state)

    def load_selected_versions(self):
        table = self.panel.versionTable
        selected_features = []

        for row in range(table.rowCount()):
            item = table.item(row, 0)
            if item.checkState() == Qt.Checked:
                feature = item.data(Qt.UserRole)
                selected_features.append(feature)

        if not selected_features:
            self.iface.messageBar().pushMessage(
                self.tr(u'Kataster'),
                self.tr(u'Vali vähemalt üks versioon.'),
                level=Qgis.Info, duration=4)
            return

        for feat in selected_features:
            self.add_version_layer(feat)

    def load_version_row(self, row, column):
        """Double-click on a table row loads just that version."""
        item = self.panel.versionTable.item(row, 0)
        feat = item.data(Qt.UserRole) if item else None
        if feat is not None:
            self.add_version_layer(feat)

    def get_layer_group(self, name):
        """Find or create a top-level layer-tree group with the given name."""
        root = QgsProject.instance().layerTreeRoot()
        group = root.findGroup(name)
        if group is None:
            group = root.insertGroup(0, name)
        return group

    def add_version_layer(self, feat):
        """Add one cadastre version as a styled memory layer, grouped by tunnus."""
        date = feat["kehtiv_alates"]
        if hasattr(date, "toString"):
            date_text = date.toString("yyyy-MM-dd")
        else:
            date_text = str(date)

        # Create memory layer holding the single version feature
        layer = QgsVectorLayer("Polygon?crs=EPSG:3301",
                               f"Versioon {date_text}", "memory")
        layer.dataProvider().addAttributes(feat.fields().toList())
        layer.updateFields()
        layer.startEditing()
        layer.addFeature(feat)
        layer.commitChanges()

        # One layer-tree group per cadastre unit keeps the Layers panel tidy.
        try:
            group_name = str(feat["tunnus"])
        except Exception:
            group_name = self.panel.tunnusInput.text().strip()
        group = self.get_layer_group(group_name)

        # Transparent fill + colored outline so stacked versions stay readable;
        # the color cycles with each layer already in the group.
        color = self.VERSION_COLORS[len(group.children()) % len(self.VERSION_COLORS)]
        symbol = QgsFillSymbol.createSimple({
            "color": "0,0,0,0",
            "outline_color": color,
            "outline_width": "0.8",
        })
        layer.renderer().setSymbol(symbol)
        layer.triggerRepaint()

        QgsProject.instance().addMapLayer(layer, False)
        group.insertLayer(0, layer)


